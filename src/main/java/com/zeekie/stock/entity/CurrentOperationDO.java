package com.zeekie.stock.entity;

public class CurrentOperationDO {

	private Float currentAsset;

	private Float actualFund;

	private Float profitAndLossCash;

	private Float profitAndLossRadio;

	private Float progressBar;

	/**
	 * @return the currentAsset
	 */
	public Float getCurrentAsset() {
		return currentAsset;
	}

	/**
	 * @param currentAsset
	 *            the currentAsset to set
	 */
	public void setCurrentAsset(Float currentAsset) {
		this.currentAsset = currentAsset;
	}

	/**
	 * @return the actualFund
	 */
	public Float getActualFund() {
		return actualFund;
	}

	/**
	 * @param actualFund
	 *            the actualFund to set
	 */
	public void setActualFund(Float actualFund) {
		this.actualFund = actualFund;
	}

	/**
	 * @return the profitAndLossCash
	 */
	public Float getProfitAndLossCash() {
		return profitAndLossCash;
	}

	/**
	 * @param profitAndLossCash
	 *            the profitAndLossCash to set
	 */
	public void setProfitAndLossCash(Float profitAndLossCash) {
		this.profitAndLossCash = profitAndLossCash;
	}

	/**
	 * @return the profitAndLossRadio
	 */
	public Float getProfitAndLossRadio() {
		return profitAndLossRadio;
	}

	/**
	 * @param profitAndLossRadio
	 *            the profitAndLossRadio to set
	 */
	public void setProfitAndLossRadio(Float profitAndLossRadio) {
		this.profitAndLossRadio = profitAndLossRadio;
	}

	/**
	 * @return the progressBar
	 */
	public Float getProgressBar() {
		return progressBar;
	}

	/**
	 * @param progressBar
	 *            the progressBar to set
	 */
	public void setProgressBar(Float progressBar) {
		this.progressBar = progressBar;
	}

}
